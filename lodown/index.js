/**
 * typeof: Designed to take a value and return the data type of it.
 * 
 * @param {Any value} value: The value to be tested in an if-else chain.
 * @param {Function} action: The Function to be applied to values to determine the data type.
*       Types are:
*          - "string"
*          - "array"
*          - "object"
*          - "undefined"
*          - "number"
*          - "boolean"
*          - "null"
*          - "function"
 */
_.typeOf = function(value){
    if(typeof value !== 'object'){
        return typeof value
    } else if(value == null){
        return 'null'
    } else if(Array.isArray(value)){
        return 'array'
    } else{
        return 'object'
    }
}

